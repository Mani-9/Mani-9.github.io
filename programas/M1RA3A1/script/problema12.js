function mostrarPrimerCaracter() {

  let selectElement = document.getElementById("Menu");
  let selectedIndex = selectElement.selectedIndex;
  let selectedOption = selectElement.options[selectedIndex].value;
  let primerCaracter = selectedOption.charAt(0);
  let tablaHtml = '<table class="table"><thead><tr><th>Descripción</th><th>Caracter</th></tr></thead><tbody>';
  tablaHtml += '<tr><td>El primer carácter es:</td><td>' + primerCaracter + '</td></tr>';
  tablaHtml += '</tbody></table>';

  document.getElementById("primerCaracter").innerHTML = tablaHtml;
}
function mostrarUltimoCaracter() {
  let selectElement = document.getElementById("Menu");
  let selectedIndex = selectElement.selectedIndex;
  let selectedOption = selectElement.options[selectedIndex].value;
  let optionLength = selectedOption.length;
  let ultimoCaracter = selectedOption.charAt(optionLength - 1);
  let tablaHtml = '<table class="table"><thead><tr><th>Descripción</th><th>Caracter</th></tr></thead><tbody>';
  tablaHtml += '<tr><td>El ultimo carácter es:</td><td>' + ultimoCaracter + '</td></tr>';
  tablaHtml += '</tbody></table>';

  document.getElementById("primerCaracter").innerHTML = tablaHtml;
}
function mostrarCaracteres() {
  let selectElement = document.getElementById("Menu");
  let selectedIndex = selectElement.selectedIndex;
  let selectedOption = selectElement.options[selectedIndex].value;
  let tablaHtml = '<table class="table"><thead><tr><th>Carácter</th></tr></thead><tbody>';
  for (let i = 0; i < selectedOption.length; i++) {
    tablaHtml += '<tr><td>' + selectedOption.charAt(i) + '</td></tr>';
  }
  tablaHtml += '</tbody></table>';

  document.getElementById("primerCaracter").innerHTML = tablaHtml;
}
